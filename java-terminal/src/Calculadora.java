public class Calculadora {
    /**
     * Este metodo e utilizado para somar dois numeros inteiros
     * @param numeroUm este é o primeiro parametro do metodo
     * @param numeroDois este é o segundo parametro do metodo
     * @return int o resultado deste metodo é a soma dos dois numeros inteiros
     * @throws Exception
     */
    public int somar(int numeroUm, int nuemroDois) {
        return numeroUm + nuemroDois;
    }
}
